    public void openSeeDetails(int row) throws SQLException {
        // Obtener el modelo de la tabla
        DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
        
        // Verificar si la fila es v√°lida
        if (row < 0 || row >= model.getRowCount()) {
            System.out.println("√çndice fuera de los l√≠mites de la tabla.");
            return;
        }

        // Obtener el ID del proyecto desde la tabla (columna oculta)
        Object idValue = tblRequests.getModel().getValueAt(row, 0); // Obtener como Object

        // Verificar el tipo y convertir
        int projectId;
        if (idValue instanceof Long) {
            projectId = ((Long) idValue).intValue(); // Convertir Long a int
        } else if (idValue instanceof Integer) {
            projectId = (Integer) idValue; // Si ya es Integer, castear directamente
        } else {
            // Manejar el caso si el tipo es inesperado (lanzar excepci√≥n o loggear error)
            throw new ClassCastException("El ID del proyecto no es ni Long ni Integer: " + idValue.getClass().getName());
        }
        // Buscar el proyecto por ID en el servicio
        Project project = projectService.getProjectById(projectId);

        if (project == null) {
            System.out.println("No se encontr√≥ el proyecto con ID: " + projectId);
            return;
        }

        // Configurar los detalles del proyecto en GUISeeDetails
        lblProjectName.setText("<html>" + project.getName() + "</html>");
        lblSummary.setText("<html>" + project.getSummary() + "</html>");
        lblGoals.setText("<html>" + project.getGoals() + "</html>");
        lblDescription.setText("<html>" + project.getDescription() + "</html>");
        lblMaxTimeInMonths.setText(String.valueOf(project.getMaxtimeMonths()));
        lblBudget.setText(String.valueOf(project.getBudget()));
        lblDate.setText(project.getDate().toString());
        lblState.setText(project.getStatus());

        // Configurar los detalles de la empresa
        
        Company company = companyService.getCompany(project.getCompanyId());
        if (company != null) {
            lblCompanyName.setText("<html>" + company.getName() + "</html>");
            lblCompanyNit.setText(company.getNIT());
            lblCompanyEmail.setText(company.getEmail());
            lblCompanySector.setText(company.getSector());
            lblCompanyContactPhone.setText(company.getContactPhoneNumber());
            lblCompanyContactNames.setText(company.getContactNames());
            lblCompanyContactLastNames.setText(company.getContactLastNames());
            lblCompanyContactPosition.setText(company.getContactPosition());
        } else {
            System.out.println("No hay informaci√≥n de la empresa asociada al proyecto.");
        }
        GUISeeDetails.pack();
        // Mostrar la ventana GUISeeDetails
        GUISeeDetails.setVisible(true);
    }

